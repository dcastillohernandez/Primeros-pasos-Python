#Ejemplo de como usar las listas.

friends=["Joey","Chandler","Ross","Phoebe","Rachel","Monica"]

print(len(friends))
print(friends[0])#imprimirá solo el prrimer elemento de nuestra lista (Así sucesivamente todos los que pongamos).
print(friends[1])
print(friends[2])
print(friends[3])
print(friends[4])
print(friends[5])
print(friends[-1])#Imprimirá solo el último elemento de la lista, y así sucesivamente lo pidamos.
print(friends[-2])
print(friends[-3])
print(friends[-4])
print(friends[-5])
print(friends[-6])
print(friends[3:])#Comenzará en el primero de los 3 ultimos elemntos de la lista (el 3 es ejemplo).
print(friends[2:5])#Comenzará en el primer numero antes de los 2 puntos y finalizará en el siguiente de los 2 puntos.
